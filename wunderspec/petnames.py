def canonical_args(args: dict[str, Any]) -> str:
    """Produce a canonical JSON string from CLI args dict (sorted keys, no whitespace)."""
    return json.dumps(args, sort_keys=True, separators=(",", ":"))


def funny_name(args: dict[str, Any]) -> str:
    """Generate a deterministic 3-word petname from CLI arguments.

    Uses SHA-256 of the canonical JSON representation to pick one word
    from each of the three lists (adjective-participle-animal).
    """
    canon = canonical_args(args)
    digest = hashlib.sha256(canon.encode("utf-8")).digest()
    # Use first 6 bytes: 2 bytes per word index
    i0 = int.from_bytes(digest[0:2], "big") % len(ADJECTIVES)
    i1 = int.from_bytes(digest[2:4], "big") % len(PARTICIPLES)
    i2 = int.from_bytes(digest[4:6], "big") % len(ANIMALS)
    return f"{ADJECTIVES[i0]}-{PARTICIPLES[i1]}-{ANIMALS[i2]}"
